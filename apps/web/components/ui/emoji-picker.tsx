interface EmojiPickerProps {
  onEmojiSelect: (emoji: string) => void;
  children?: React.ReactNode;
}

export function EmojiPicker({ onEmojiSelect, children }: EmojiPickerProps) {
  const [search, setSearch] = useState("");
  const [open, setOpen] = useState(false);

  const filteredEmojis = Object.entries(emojiCategories).reduce((acc, [category, emojis]) => {
    const filtered = emojis.filter((emoji) =>
      search ? emoji.includes(search) : true
    );
    if (filtered.length > 0) {
      acc[category] = filtered;
    }
    return acc;
  }, {} as Record<string, string[]>);

  const handleEmojiClick = (emoji: string) => {
    onEmojiSelect(emoji);
    setOpen(false);
    setSearch("");
  };

  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger asChild>
        {children || (
          <Button variant="outline" size="sm" className="gap-2">
            <Smile className="w-4 h-4" />
            Emoji
          </Button>
        )}
      </PopoverTrigger>
      <PopoverContent className="w-[350px] p-0" align="start">
        <div className="p-3 border-b">
          <Input
            placeholder="Search emojis..."
            value={search}
            onChange={(e) => setSearch(e.target.value)}
            className="h-8"
          />
        </div>
        <ScrollArea className="h-[300px]">
          <div className="p-2">
            {Object.entries(filteredEmojis).map(([category, emojis]) => (
              <div key={category} className="mb-4">
                <h4 className="text-xs font-semibold text-muted-foreground mb-2 px-2">
                  {category}
                </h4>
                <div className="grid grid-cols-8 gap-1">
                  {emojis.map((emoji, index) => (
                    <button
                      key={`${emoji}-${index}`}
                      onClick={() => handleEmojiClick(emoji)}
                      className="w-9 h-9 flex items-center justify-center text-xl hover:bg-accent rounded transition-colors"
                    >
                      {emoji}
                    </button>
                  ))}
                </div>
              </div>
            ))}
            {Object.keys(filteredEmojis).length === 0 && (
              <div className="text-center text-muted-foreground py-8">
                No emojis found
              </div>
            )}
          </div>
        </ScrollArea>
      </PopoverContent>
    </Popover>
  );
}
